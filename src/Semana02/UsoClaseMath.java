package Semana02;

public class UsoClaseMath {
    public static void main(String[] args) {
        double x = 4.5;
        double y = -3.0;
        double z = 5.7;

        // Ejemplo de algunas funciones de la clase Math
        System.out.println("Valor absoluto de " + y + ": " + Math.abs(y)); // Valor absoluto
        System.out.println("Máximo entre " + x + " y " + z + ": " + Math.max(x, z)); // Máximo
        System.out.println("Mínimo entre " + x + " y " + z + ": " + Math.min(x, z)); // Mínimo
        System.out.println("Potencia de " + x + " elevado a " + z + ": " + Math.pow(x, z)); // Potencia
        System.out.println("Redondeo de " + x + ": " + Math.round(x)); // Redondeo
        System.out.println("Seno de " + x + " en radianes: " + Math.sin(x)); // Seno
        System.out.println("Coseno de " + x + " en radianes: " + Math.cos(x)); // Coseno
        System.out.println("Tangente de " + x + " en radianes: " + Math.tan(x)); // Tangente
        System.out.println("Valor de PI: " + Math.PI); // Valor de PI
        System.out.println("Raíz cuadrada de " + x + ": " + Math.sqrt(x)); // Raíz cuadrada
        System.out.println("Logaritmo natural de " + z + ": " + Math.log(z)); // Logaritmo natural
        System.out.println("Logaritmo base 10 de " + z + ": " + Math.log10(z)); // Logaritmo base 10
    }
}

