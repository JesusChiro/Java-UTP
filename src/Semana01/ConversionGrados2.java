public class ConversionGrados2 {
    public static void main(String[] args) {
        double gradosCentigrados = 39.5;
        double gradosFarenheit = (gradosCentigrados * 9 / 5) + 32;

        System.out.println(gradosCentigrados + " grados centigrados son " + gradosFarenheit + " grados Farenheit.");
    }
}
